function goUrl(z){
    switch(z){
        case 'familySite':
            location.href = '';
            break

        case 'insta':
            window.open('https://www.instagram.com');
            break

        case 'demo':
            location.href = 'https://lithohtml.themezaa.com/home-furniture-shop.html';
            break
        default:
            alert();
    }
}